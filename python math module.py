# importing math module
import math
print("Value of Pi=", math.pi) #pi
print("cos(pi) =", math.cos(math.pi)) #cosine of pi
print("e^5 =", math.exp(5)) #exponential of 5
print("log10(10000) =", math.log10(10000)) #logarithm of 10000 to the base 10
print("sinh(1/2) =", math.sinh(1/2))     #hyperbolic sine of 1/2
print("7! =", math.factorial(7)) #factorial of 7